    <h4>Browser Fingerprint:</h4>
    <ul>
      <li>Collection of all/some/relevant feature information from the browser</li>
      <li>Super sneaky</li>
      <li>Not really a lot that can be done against it</li>
    </ul>

    <aside class="notes">
      <ul>
        <li>already gave a brief introduction to the browser fingerprint</li>
        <li>
          wanna say a little bit more about it, and why I want to use it and not another tracking
          method
        </li>
        <li>
          in my research I found out, that it is the most sneaky tracking method, because a website
          can collect the fingerprint without leafing any traces on your system unlike with cookies
          -> the cookie, or with the tracking pixel -> the image of the tracking pixel
        </li>
        <li>also it is the hardest tracking method to escape from</li>
        <li>
          cookies can just be deleted (there are extensions for it or in the browser settings,
          example when closing the browser)
        </li>
        <li>
          tracking pixels can be blocked from loading -> also browser extensions or in the email
          settings -> not loading remote content
        </li>
        <li>
          for fingerprinting its just not that easy -> the code for collecting a fingerprint can be
          directly included into the code, that is also required for running the website so it can
          not be blocked
        </li>
        <li>
          also when users modify their browsers to give false information about some of the
          attributes it is also detectable, because the companies behind the fingerprinting code
          became so good, as to read from the other attributes of the BF what the actual values must
          have been (and also they can detect browser extensions)
        </li>
        <li>
          so yeah, thats why I chose this method -> it's the most interesting one and some of the
          things that can be found out really shocked me
        </li>
      </ul>

